#include "libft.h"

char	*ft_strcat(char *str1, const char *str2)
{
	size_t	i;
	size_t	n;

	i = 0;
	n = 0;
	while (str1[i] != '\0')
		i++;
	while (str2[n] != '\0')
	{
		str1[i] = str2[n];
		i++;
		n++;
	}
	str1[i] = '\0';
	return (str1);
}
